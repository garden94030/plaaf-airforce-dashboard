function replaceKnownTerms(text) {
  if (!isEnglishMode() || typeof text !== 'string') return text;
  const pairs = Object.entries(EN_MAPS.aircraft).sort((a, b) => b[0].length - a[0].length);
  let result = text;
  pairs.forEach(([zh, en]) => {
    result = result.replaceAll(zh, en);
  });
  result = result.replaceAll('空對空飛彈', 'AAMs');
  result = result.replaceAll('反艦飛彈', 'anti-ship missiles');
  result = result.replaceAll('精確導引炸彈', 'precision-guided bombs');
  result = result.replaceAll('雷射導引炸彈', 'laser-guided bombs');
  result = result.replaceAll('內置彈艙', 'internal weapons bay');
  result = result.replaceAll('精確導引彈藥', 'precision-guided munitions');
  result = result.replaceAll('等多型戰機', ' and multiple aircraft types');
  result = result.replaceAll('等160+架', ' and 160+ aircraft');
  return result;
}

